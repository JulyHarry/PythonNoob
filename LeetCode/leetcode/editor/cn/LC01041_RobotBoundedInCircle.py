class Solution:
    def isRobotBounded(self, instructions: str) -> bool:
        dirc = [(0, 1), (1, 0), (0, -1), (-1, 0)]

        def go(s, x, y, d):
            if s == 'L':
                d -= 1
                if d == -1:
                    d = 3
                return x, y, d
            elif s == 'R':
                if d == 4:
                    d = 0
                return x, y, d
            else:
                x += dirc[d][0]
                y += dirc[d][1]
                return x, y, d

        x1, y1, d1 = 0, 0, 0
        x2, y2, d2 = 0, 0, 0
        for i in range(len(instructions)):
            x1, y1, d1 = go(instructions[i], x1, y1, d1)
            x2, y2, d2 = go(instructions[(2 * i) % len(instructions)], x2, y2, d2)
            if x1 == x2 and y1 == y2:
                return True

        return False

